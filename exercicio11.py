#  Faça um algoritmo para ler dois números inteiros e informar se estes números são iguais ou diferentes

# Declarações
num1 = int(input("Digite o primeiro valor: "))
num2 = int(input("Digite o segundo valor: "))

# Decisão se o num1 for igual a num2
if num1 == num2:
    print("Os números digitados são iguais.")
# Decisão se não for igual
else:
    print("Os números digitados são diferentes.")